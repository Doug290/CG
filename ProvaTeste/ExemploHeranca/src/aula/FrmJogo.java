    @Override
    public void run() {
        Game game = new Game(getWidth(), getHeight());
   
        while (true) {
            Graphics g = getBufferStrategy().getDrawGraphics();
            BufferStrategy buffer = getBufferStrategy();

            g.setFont(new Font("Dialog", Font.ITALIC, 25));
            
            game.update(g);
            game.updateScore(g);
            game.setPlayerActions(direitaPressed, esquerdaPressed);
            
            g.dispose();
            buffer.show();

            try {
                Thread.sleep(10);
            } catch (InterruptedException ex) {

            }
        }   
    }
    
